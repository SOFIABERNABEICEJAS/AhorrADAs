let seccionNuevaOperacion = document.getElementById("section-nueva-operacion")
let botonNuevaOperacion = document.getElementById("boton-nueva-operacion")
let seccionPrincipal = document.getElementById("seccion-principal")









// boton nueva operacion --- abrir seccion nueva operacion //


botonNuevaOperacion.onclick = () => {
    seccionNuevaOperacion.classList.add = "is-invisible"
    seccionNuevaOperacion.classList.remove = "is-hidden"
    seccionPrincipal.classList.add = "is-invisible"



}  



